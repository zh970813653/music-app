<template>
  <div class="search">
    <div class="search-box-wrapper">
      <search-box @query="onQueryChange" ref="searchBox"></search-box>
    </div>
    <div ref="shortcutWrapper" class="shortcut-wrapper" v-show="!query">
      <scroll :data="shortcut" class="shortcut" ref="shortcut">
        <div>
          <div class="hot-key">
            <h1 class="title">热门搜索</h1>
            <ul>
              <li
                @click="addQuery(item.k)"
                class="item"
                v-for="(item, index) in hotKey"
                :key="index"
              >
                <span>{{ item.k }}</span>
              </li>
            </ul>
          </div>
          <div class="search-history" v-show="searchHistory.length">
            <h1 class="title">
              <span class="text">搜索历史</span>
              <span class="clear" @click="clear">
                <i class="icon-clear"></i>
              </span>
            </h1>
            <search-list
              :searches="searchHistory"
              @delete="deleteSearchHistory"
              @select="addQuery"
            ></search-list>
          </div>
        </div>
      </scroll>
    </div>
    <div class="search-result" ref="searchResult" v-show="query">
      <suggest
        @saveSearch="saveSearchHistory(query)"
        @listScroll="blurInput"
        ref="suggest"
        :query="query"
      ></suggest>
    </div>
    <confirm
      @confirm="clearSearchHistory"
      ref="confirm"
      text="您确定要全部清空吗"
    ></confirm>
    <router-view></router-view>
  </div>
</template>

<script>
import SearchBox from 'base/search-box/search-box'
import Suggest from 'components/suggest/suggest'
import SearchList from 'base/search-list/search-list'
import Confirm from 'base/confirm/confirm'
import Scroll from 'base/scroll/scroll'
import { playlistMixin } from 'common/mixin'
import { getHotKey, ERR_OK } from 'api/search'
import { mapActions, mapGetters } from 'vuex'
export default {
  mixins: [playlistMixin],
  components: {
    SearchBox,
    Suggest,
    SearchList,
    Scroll,
    Confirm
  },
  data () {
    return {
      query: '',
      hotKey: []
    }
  },
  computed: {
    ...mapGetters(['searchHistory']),
    shortcut () {
      return this.hotKey.concat(this.searchHistory)
    }
  },
  created () {
    this._getHotKey()
  },
  methods: {
    ...mapActions([
      'saveSearchHistory',
      'deleteSearchHistory',
      'clearSearchHistory'
    ]),
    async _getHotKey () {
      const res = await getHotKey()
      if (res && res.code === ERR_OK) {
        console.log(res)
        this.hotKey = res.data.hotkey.slice(0, 10)
      }
    },
    addQuery (k) {
      this.$refs.searchBox.setQuery(k)
    },
    onQueryChange (query) {
      this.query = query
    },
    handlePlaylist (playlist) {
      const bottom = playlist.length > 0 ? 60 : ''
      this.$refs.shortcutWrapper.style.bottom = bottom + 'px'
      this.$refs.shortcut.refresh()
      this.$refs.searchResult.style.bottom = bottom + 'px'
      this.$refs.suggest.refresh()
    },
    clear () {
      this.$refs.confirm.show()
    },
    blurInput () {
      this.$refs.searchBox.blur()
    }
  },
  watch: {
    query (newQuery) {
      if (!newQuery) {
        setTimeout(() => {
          this.$refs.shortcut.refresh()
        }, 20)
      }
    }
  }
}
</script>

